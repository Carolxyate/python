C = int(input("Ingrese su valor en grados celsius")) #C = Celsius

F = C * 1.8 + 32      #Formula de C a farenheit
print("El valor", C, "En grados Farenheit es: ", F)

K = C + 273.15        #Formula para convertir C a Kelvin
print("El valor", C, " en grados Kelvin es", K)

R = C * 9/5 + 491.67  #Formul de Rankine
print("El valor", C, "en grados Rankine es", R)
